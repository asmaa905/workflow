// Initialize workflows from localStorage or use default values
const workflows = ref([]);

// Load workflows from localStorage when component mounts
onMounted(() => {
  const savedWorkflows = localStorage.getItem('workflows');
  if (savedWorkflows) {
    workflows.value = JSON.parse(savedWorkflows);
  } else {
    // Default workflows if none exist in localStorage
    workflows.value = [
    
      { id: 1, name: "subscribe order", nodesCount: 5 },
      { id: 2, name: "checkout order", nodesCount: 10 },
    ];
    saveWorkflows(); // Save default workflows to localStorage
  }
});

// Save workflows to localStorage
const saveWorkflows = () => {
  localStorage.setItem('workflows', JSON.stringify(workflows.value));
};

// Delete a workflow by ID
const deleteWorkflow = (id) => {
  workflows.value = workflows.value.filter(workflow => workflow.id !== id);
  saveWorkflows(); // Update localStorage after deletion
};

// Function to confirm and delete a workflow
const confirmDelete = (id) => {
  if (confirm('Are you sure you want to delete this workflow?')) {
    deleteWorkflow(id);
  }
};

// If you need component name for devtools
defineOptions({
  name: "WorkflowShow"
});
const generateId = () => Date.now();
const addWorkflow = (workflow) => {
  workflows.value.push({ ...workflow, id: generateId() });
  saveWorkflows();
};
const editWorkflow = (updatedWorkflow) => {
  const index = workflows.value.findIndex(w => w.id === updatedWorkflow.id);
  if (index !== -1) {
    workflows.value[index] = updatedWorkflow;
    saveWorkflows();
  }
};
